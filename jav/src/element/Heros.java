package element;

public class Heros {
private int x;
private int y;
private int rayon;

public int getX() {
	return x;
}

public void setX(int x) {
	this.x = x;
}

public int getY() {
	return y;
}

public void setY(int y) {
	this.y = y;
}

public int getRayon() {
	return rayon;
}

public void setRayon(int rayon) {
	this.rayon = rayon;
}

public Heros (int x, int y , int rayon){
	this.x=x;
	this.y=y;
	this.rayon=rayon;
}
}
